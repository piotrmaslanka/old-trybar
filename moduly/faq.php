<b>Kto może dodawać lokale do bazy?</b><br>
- Do bazy lokale może dodawać każdy. Wystarczy się zarejestrować pod tym adresem: <a href="http://www.trybar.pl/rejestracja.php">www.trybar.pl/rejestracja.php</a><br>
<br>
<b>Za co się otrzymuje punkty?</b><br>
- Punkty się otrzymuje za dodawanie zdjęć do lokali i prywatnej galerii, za dodawanie lokali do naszej bazy, a także za pisanie komentarzy.<br>
<br>
<b>Ile punktów się otrzymuje za poszczególne elementy?</b><br>
- Poszczególne elementy mają następujące wartości:<br>
	Zdjęcie lokalu 20pkt<br>
	Lokal 10pkt<br>
	Zdjęcie do prywatnej galerii 5pkt<br>
	Komentarz 1pkt<br>
<br>	
<b>Co trzeba zrobić by otrzymać nagrodę?</b><br>
- Trzeba się znaleźć w czołówce rankingu pod względem zdobytych punktów na przestrzeni miesiąca. <br>
<br>
<b>Jakie nagrody można otrzymać?</b>
- Lista nagród znajduje się pod tym adresem: <a href="http://www.trybar.pl/nagrody.php">www.trybar.pl/nagrody.php</a><br>
<br>
<b>Jak przebiega proces otrzymywania nagrody?</b><br>
- Co miesiąc wybieramy najaktywniejszych użytkowników na podstawie zdobytych punktów na przestrzeni całego miesiąca. Sprawdzamy co użytkownik chciałby dostać i kontaktujemy się z nim mailowo z prośbą o podanie danych adresowych. Na podany adres wysyłamy paczkę z upragnioną nagrodą i zaczynamy zabawę od początku. Może się tak zdarzyć, że którejś nagrody zabraknie. Cały system opracowaliśmy tak by tych nagród nie brakowało jednak jest mała szansa, że tak może się zdarzyć. Wtedy dodatkowo kontaktujemy się z użytkownikiem z prośbą o wybranie innej nagrody. W przypadku Goblinowskich kuponów użytkownik zostanie poinstruowany mailowo co dalej.<br>
<br>
<b>Co jaki okres czasu są rozdawane nagrody?</b><br>
- Co miesiąc wybieramy laureata lub laureatów więc każdy ma szansę na znalezienie się w gronie szczęśliwców.<br>
<br>
<b>Czy można do jednego lokalu dodać więcej niż jedno zdjęcie?</b><br>
- Tak. By to zrobić trzeba kliknąć w statystykę „Bary” po prawej stronie awatara. Z wyświetlonej listy lokali kliknąć przycisk „Zarządzaj” pod danym lokalem i na końcu wybrać zdjęcie z dysku komputera.<br>
<br>
<b>Ile zdjęć maksymalnie można dodać do galerii lokalu?</b><br>
-Na chwilę obecną liczba jest nieograniczona. Jednak zdjęcia są przeglądane przez moderatora i przesadna ilość lub zdjęcia o nieodpowiedniej treści będą usuwane.<br>
