<b>Wstęp</b>

<p>Niniejszy Regulamin został sporządzony w oparciu o przepisy
prawa obowiązujące na terytorium Rzeczpospolitej Polskiej i określa zasady
działania platformy komunikacyjnej (serwisu społecznościowego), funkcjonującej
pod domeną www.trybar.pl oraz
świadczenia i korzystania z Usług. Regulamin określa prawa i obowiązki
Użytkowników.</p>

<p >Warunkiem korzystania z Usług jest dokładne zapoznanie się z
treścią Regulaminu (wraz z załącznikami).</p>

<p >Nazwa Serwisu, jego koncepcja, wygląd graficzny, logo,
elementy grafiki, oprogramowanie oraz baza danych podlegają ochronie prawnej.</p>



<p ><b style='mso-bidi-font-weight:normal'>Definicje<o:p></o:p></b></p>

<p >Użyte w Regulaminie pojęcia oznaczają:</p>

<p >Baza Kont - zbiór Danych lub Danych Osobowych zamieszczonych
w Serwisie przez Użytkowników, które za zgodą Użytkowników są gromadzone,
przetwarzane w uporządkowany sposób w systemie informatycznym, w ramach Usług
świadczonych przez Portal;</p>

<p >Baza Lokali - zbiór Danych lub Danych Lokali zamieszczonych
w Serwisie przez Użytkowników, które za zgodą Użytkowników są gromadzone,
przetwarzane w uporządkowany sposób w systemie informatycznym, w ramach Usług
świadczonych przez Portal;</p>

<p >Dane – treści, w szczególności informacje, zdjęcia,
komentarze, które Użytkownik dobrowolnie umieszcza w Serwisie;</p>

<p >Dane Osobowe - zgromadzone w ramach formularza
rejestracyjnego dostępnego pod adresem www.<span >trybar</span>.<span
>pl</span>/rejestracja informacje dotyczące zidentyfikowania Użytkownika,
przetwarzane przez Portal w celu prawidłowego świadczenia Usług określonych w
szczególności w Regulaminie Serwisu trybar.<span >pl</span>, jak
również w celach operacyjnych i statystycznych związanych z prowadzeniem
Serwisu oraz w innych celach wskazanych w odpowiedniej zgodzie udzielonej przez
Użytkownika;</p>

<p >Dane Lokali - zgromadzone w ramach formularza dodawania baru
dostępnego pod adresem <span >www</span>.<span >trybar</span>.<span
>pl</span>/dodaj informacje dotyczące zidentyfikowanego lub
możliwego do zidentyfikowania Lokalu, przetwarzane przez Portal w celu
prawidłowego świadczenia Usług określonych w szczególności w Regulaminie
Serwisu trybar.<span >pl</span>, jak również w celach operacyjnych i
statystycznych związanych z prowadzeniem Serwisu oraz w innych celach
wskazanych w odpowiedniej zgodzie udzielonej przez Użytkownika;</p>

<p ><span >Konto<span style='mso-spacerun:yes'> 
</span>– udostępnione</span> przez Portal Użytkownikowi miejsce na serwerze, na
które Użytkownik wprowadza Dane i w ramach którego administruje Danymi, z którymi
mogą się zapoznać inni Użytkownicy, które również może wykorzystywać Portal na
warunkach określonych w niniejszym Regulaminie oraz stosownych zgodach danego
Użytkownika. Użytkownik decyduje o celach i środkach przetwarzania Danych w
ramach jego Konta. W zakresie Konta Użytkownik, z zastrzeżeniem postanowień
niniejszego Regulaminu, uzyskuje dostęp do wszystkich Usług w ramach Portalu
(za wyjątkiem Usług, które wymagają dodatkowej akceptacji lub dokonania innej
czynności prawnej);</p>

<p >Login/Nazwa Użytkownika - ciąg znaków wprowadzonych przez
Użytkownika w Serwisie na etapie tworzenia Konta, umożliwiający późniejszą
identyfikację Użytkownika podczas korzystania z Serwisu. Login, jako
techniczny, unikalny ciąg znaków, nie ulega zmianie;</p>

<p >Nazwa Lokalu - ciąg znaków wprowadzonych przez Użytkownika w
Serwisie na etapie dodawania Lokalu, umożliwiający późniejszą identyfikację
Lokalu podczas korzystania z Serwisu. Nazwa Lokalu, jako techniczny, unikalny
ciąg znaków, nie ulega zmianie;</p>

<p >Serwis/Portal - portal internetowy prowadzony pod domeną trybar.<span
>pl</span>;</p>

<p >Shoutbox - bezpośredni komunikator internetowy dostępny dla
Użytkowników służący komunikacji wewnętrznej w ramach Serwisu. Regulamin usługi
Komunikator znajduje się w Załączniku nr 4 do Regulaminu Serwisu nk.<span
>pl</span>;</p>

<p >Osoba - osoba fizyczna </p>

<p >Lokal – osoba prawna, a także jednostka organizacyjna
nieposiadająca osobowości prawnej, której przepisy prawa przyznają zdolność
prawną;</p>

<p >Poczta - usługa dostępna dla Użytkowników służąca
komunikacji wewnętrznej w ramach Serwisu, przystosowana do wyświetlania
wiadomości;</p>

<p >Regulamin - niniejszy dokument określający zasady
korzystania z Serwisu, w tym w szczególności prawa i obowiązki Użytkowników
wraz z załącznikami stanowiącymi jego integralną część;</p>

<p >Spam - wiadomości elektroniczne (w szczególności o
charakterze komercyjnym) przesyłane w ramach Portalu za pośrednictwem Poczty,
Shoutboxa, Komentarzy lub w Opisach Lokali jak również umieszczane w innych
miejscach Portalu, na których otrzymywanie odbiorca nie wyraził uprzedniej
zgody;</p>

<p >Komentarz – wiadomość elektroniczna przesyłana w ramach
Portalu pod różnymi elementami będącymi integralną częścią portalu.; Opis
Lokalu – tekst elektroniczny dodawany w procesie dodawania Lokalu;</p>

<p >Umowa - umowa o świadczenie usług drogą elektroniczną
zawarta między Użytkownikiem a Portalem z chwilą akceptacji Regulaminu przez
Użytkownika, o treści odpowiadającej treści Regulaminu;</p>

<p >Usługa/Usługi - wszelkie świadczenia Portalu na rzecz
Użytkowników wykonywane w ramach Umowy, w tym w szczególności usługa dodawania
Lokali. Użytkownik zawierając Umowę wyraża zgodę na wyświetlanie przez Portal
Usług na Koncie Użytkownika oraz ich komunikowanie za pomocą usługi Poczty;</p>

<p >Użytkownik - osoba posiadająca pełną zdolność do czynności
prawnych, która poprzez akceptację Regulaminu uzyskała dostęp do Usług.
Użytkownikiem nie może być osoba fizyczna małoletnia;</p>



